#ifndef EBO_H
#define EBO_H

class EBO {
public:
	// Reference ID to this EBO object.
	GLuint ID;
	/*!***********************************************************************
	\brief
		Construct a element buffer array object.
	*************************************************************************/
	EBO(GLuint* indices, GLsizeiptr size);

	/*!***********************************************************************
	\brief
		Set this EBO as active.
	*************************************************************************/
	void Bind();

	/*!***********************************************************************
	\brief
		Deactive this EBO.
	*************************************************************************/
	void Unbind();

	/*!***********************************************************************
	\brief
		Delete this EBO.
	*************************************************************************/
	void Delete();
};

#endif // !EBO_H
