#ifndef VBO_H
#define VBO_H

class VBO {
public:
	// Reference ID to this VBO obj.
	GLuint ID;
	/*!***********************************************************************
	\brief
		Construct a vertex buffer object.
	*************************************************************************/
	VBO(GLfloat* vertices, GLsizeiptr size);

	/*!***********************************************************************
	\brief
		Set this VBO as active.
	*************************************************************************/
	void Bind();

	/*!***********************************************************************
	\brief
		Deactive this VBO.
	*************************************************************************/
	void Unbind();

	/*!***********************************************************************
	\brief
		Delete this VBO.
	*************************************************************************/
	void Delete();
};

#endif // !VBO_H
